"""
===============================================================================
Knowledge Query Operators
===============================================================================
"""

from __future__ import annotations

from enum import Enum

###############################################################################
class FilterOperator(str, Enum):
    ###########################################################################
    # Equality
    ###########################################################################
    EQ = "eq"
    NE = "ne"

    ###########################################################################
    # Numeric
    ###########################################################################
    GT = "gt"
    GTE = "gte"
    LT = "lt"
    LTE = "lte"

    ###########################################################################
    # Collection
    ###########################################################################
    IN = "in"
    CONTAINS = "contains"
